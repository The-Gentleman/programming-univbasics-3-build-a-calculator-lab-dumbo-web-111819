def addition(num1, num2)
 sum = num1 + num2
 puts sum 
end

def subtraction(num1, num2)
 diff = num1 - num2
 puts diff
end

def division(num1, num2)
 quotient = num1 / num2
 puts quotient
end

def multiplication(num1, num2)
 product = num1 * num2
 puts product
end

def modulo(num1, num2)
 mod = num1 % num2
 puts mod
end

def square_root(num)
 sqa = Math.sqrt(num)
 puts sqa
end
